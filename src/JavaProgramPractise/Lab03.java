package JavaProgramPractise;


//Operators - Assignment operators
public class Lab03 {
    public static void main(String[] args) {

        int a = 10;
        int b = 20;
        int c = 30;
        int d = 40;
        int e = 50;



        a +=10;
        b -=5;
        c *=2;
        d /=10;
        e %=7;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

    }
}
