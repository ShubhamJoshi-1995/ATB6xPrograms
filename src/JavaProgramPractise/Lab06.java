package JavaProgramPractise;


//Operators - Ternary
public class Lab06 {
    public static void main(String[] args) {

        int a  = 10;
        int b = 20;
        int max ;
        int min;

        max = a > b ? a : b   ;
        min = a < b ? a  : b  ;
        System.out.println("Maximum from two number is : "+max);
        System.out.println("Minimum from two number is : "+min);




    }
}
